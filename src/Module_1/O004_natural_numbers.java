package Module_1;

/**
 * Write a program in Java to display the first 10 natural numbers using while
 * loop.
 *
 */
public class O004_natural_numbers {
	public static void main(String[] args) {
		System.out.println("Natural no.");
		int n = 1;

		while (n < 11) {
			System.out.println(n);
			n++;
		}
	}
}
